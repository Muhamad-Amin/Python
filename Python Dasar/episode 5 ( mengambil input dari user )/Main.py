# Episode input user


# data yang di masukkan pasti string
data = input('Masukkan nama : ')

print('nama =', data, 'Type =', type(data))

# data jika ingin int atau float, maka
data_int = int(input('Masukkan angka : '))

print('data =', data_int, 'Type =', type(data_int))

data_float = float(input('Masukkan angka : '))

print('data =', data_float, 'Type =', type(data_float))

# data boolean
data_boolean = bool(input('Masukkan nilai boolean : '))

print('data =', data_boolean, 'Type =', type(data_boolean))

